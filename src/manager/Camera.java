package manager;

public class Camera {

    private double x, y;

    public Camera(){
        this.x = 0;
        this.y = 0;
    }

    public double getX() {
        return x;
    }

    public void setX(double x) {
        this.x = x;
    }

    public double getY() {
        return y;
    }

    public void setY(double y) {
        this.y = y;
    }

    public void moveCam(double xAmount, double yAmount){
        x = x + xAmount;
        y = y + yAmount;
    }
}
